# Escribir un programa que solicite al usuario ingresar tres numero
# para luego mostrale el promedio de los tres

N1 = int(input("Ingresar el primer numero: "))
N2 = int(input("Ingresar el segundo numero: "))
N3 = int(input("Ingresar el tercer numero: "))
R = N1 + N2 + N3
P = R / 3
print("El resultado de la suma es", R)
print("El promedio es:" , P)